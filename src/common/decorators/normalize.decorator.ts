import { Transform } from 'class-transformer';

/**
 * Нормализира имейл при вход: маха водещи/крайни интервали и сваля в малки букви.
 * Прилага се както при логин/забравена парола, така и при създаване на потребител,
 * за да съвпада записаната стойност с тази при вход (иначе случаен интервал от
 * мобилна клавиатура/paste дава „грешни данни").
 */
export const NormalizeEmail = () =>
  Transform(({ value }) =>
    typeof value === 'string' ? value.trim().toLowerCase() : value,
  );

/**
 * Маха само водещи/крайни интервали (напр. на парола). Интервалите в средата се
 * запазват — те могат да са легитимна част от паролата. Прилага се консистентно
 * при задаване (регистрация/смяна/ресет) и при вход, за да не заключва потребители.
 */
export const TrimEnds = () =>
  Transform(({ value }) => (typeof value === 'string' ? value.trim() : value));
